        const buttons = document.querySelectorAll("button");
        const buttonsArr = Array.from(buttons);
        function getMobileOperatingSystem() {
            const userAgent =
                navigator.userAgent || navigator.vendor || window.opera;
            let isIOS = false,
                isAndroid = false,
                isWindowsPhone = false;

            // Windows Phone must come first because its UA also contains "Android"
            if (/windows phone/i.test(userAgent)) {
                isWindowsPhone = true;
            }

            if (/android/i.test(userAgent)) {
                isAndroid = true;
            }

            // iOS detection from: http://stackoverflow.com/a/9039885/177710
            if (/iPad|iPhone|iPod/.test(userAgent) && !window.MSStream) {
                isIOS = true;
            }

            return { isIOS, isAndroid, isWindowsPhone };
        }

        const { isIOS, isAndroid } = getMobileOperatingSystem();

        if (isIOS) document.getElementById("android_btn")?.remove();
        if (isAndroid) document.getElementById("ios_btn")?.remove();

        buttonsArr.forEach((btn) => {
            btn.onclick = () => {
                switch (btn.id) {
                    case "ios_btn":
                        return window.open(
                            "https://apps.apple.com/app/apple-store/id1608870673?pt=119567154&ct=nicegram.app_deeplink&mt=8"
                        );
                    case "android_btn":
                        return window.open(
                            "https://play.google.com/store/apps/details?id=app.nicegram&utm_source=nicegram.app&utm_medium=main&utm_campaign=deepling"
                        );
                    default:
                        break;
                }
            };
        });
